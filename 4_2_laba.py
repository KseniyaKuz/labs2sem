a = int(input('Введите номер места'))
if a <= 36:
    if a % 2 == 0: print('Ваше место - нижнее купе')
    if a % 2 != 0: print('Ваше место - верхнее купе')
if 54 <= a <= 36:
    if a % 2 == 0: print('Ваше место - боковое нижнее')
    if a % 2 != 0: print('Ваше место - боковое верхнее')
else:
    print('ошибка')

    
